import "./Page.css";
import { useState } from "react";
import { Button } from "@mui/material";
import { useNavigate } from "react-router-dom";
import DisplayCard from "./DisplayCard";

const Demo = () => {
  const [result, setResult] = useState("");
  const [accuracy, setAccuracy] = useState("");
  const [isImageSelected,setIsImageSelected] = useState(false);
  const resultTrue = "NOT GENERATED BY AI";
  const resultFalse = "GENERATED BY AI";
  const navigate = useNavigate();

  const images = [
    { src: "/chat.jpg", statut: resultTrue, accuracy: "87" },
    { src: "/chat_ia.webp", statut: resultFalse, accuracy: "91" }, //TODO : mettre les vraies accuracy du modÃ¨le
    { src: "/oiseau_ia.jpg", statut: resultFalse, accuracy: "89" },
    { src: "/oiseau.jpg", statut: resultTrue, accuracy: "85" },
  ];

  const handleImageClick = (statut: string, accuracy: string) => {
    setIsImageSelected(true);
    setResult(statut);
    setAccuracy(accuracy); 
  };

  return (
    <div className="page">
        <div className="instructions">
            <p>Select one of the following image to see if it is authentic or generated by an AI</p>
        </div>
        <div
        style={{
            display: "grid",
            gridTemplateColumns: "repeat(2, 1fr)",
            gap: "20px",
            justifyItems: "center",
            maxWidth: "800px",
            margin: "0 auto",
          }}
      >
        {images.map((image) => (
          <img
          key={image.src}
          src={image.src}
          alt="Preview"
          className="image" 
          onClick={() => handleImageClick(image.statut, image.accuracy)}
          />
        ))}
      </div>
        {isImageSelected ? (
          <div className="card">
            <DisplayCard result={result} accuracy={accuracy} />
          </div>
        ) : (null)} 
        <Button
        variant="contained"
        onClick={() => navigate("/home")}
        sx={{
            marginTop:"20px",
            backgroundColor: "#65b2a0",
            color: "#fff",
            "&:hover": { backgroundColor: "#386258" },
        }}
        >
        Home page
        </Button>
    </div>
  );
};

export default Demo;
